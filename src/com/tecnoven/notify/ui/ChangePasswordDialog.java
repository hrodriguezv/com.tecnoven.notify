	private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton1ActionPerformed
		// TODO add your handling code here:
		this.jButton1.setEnabled(false);
		String pass1 = String.valueOf(jPasswordField1.getPassword());
		String pass2 = String.valueOf(jPasswordField2.getPassword());
		String pwdLogged;
		try {
			pwdLogged = FileUtil.getHashValue(pass1);
			if (pass1.equals(pass2) && (!pwdLogged.equals(this.userLogged.getPassword()))) {
				SwingUtilities.invokeLater(new Runnable() {
					@Override
					public void run() {
						try {
							action();
						} catch (NoSuchAlgorithmException e) {
						}
					}
				});
			} else {
				this.jButton1.setEnabled(true);
				JOptionPane.showConfirmDialog(this, "Clave Inv√°lida",
						"Error", JOptionPane.DEFAULT_OPTION,
						JOptionPane.ERROR_MESSAGE);
				this.jPasswordField1.setText("");
				this.jPasswordField2.setText("");
				this.jPasswordField1.requestFocus();
			}
		} catch (NoSuchAlgorithmException e1) {
			// TODO Auto-generated catch block
			e1.printStackTrace();
		}
	}// GEN-LAST:event_jButton1ActionPerformed

	private void action() throws NoSuchAlgorithmException {
		this.userLogged.setLogged(true);
		this.userLogged.setPassword(FileUtil.getHashValue(String
				.valueOf(jPasswordField1.getPassword())));
		ConnectionDB.getInstance().getSession().update(this.userLogged);
		ConnectionDB.getInstance().getSession().flush();
		JOptionPane.showConfirmDialog(this, "Su clave ha sido actualizada",
				"Informaci\u00f3n", JOptionPane.DEFAULT_OPTION,
				JOptionPane.INFORMATION_MESSAGE);
		setVisible(false);
		dispose();
	}

